import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const audioPath = path.join(root, "public/assets/audio/home-bgm.mp3");
const outPath = path.join(root, "src/lib/audio/bgm-asset-version.ts");

const hash = createHash("md5").update(readFileSync(audioPath)).digest("hex").slice(0, 12);

const contents = `/** Generated by scripts/sync-bgm-version.mjs — do not edit manually. */\nexport const BGM_ASSET_VERSION = "${hash}";\n`;

writeFileSync(outPath, contents);
